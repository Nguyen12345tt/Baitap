/* Một số vấn đề cơ bản liên quan đến hàm
- Chia bài toán lớn thành các bài toán nhỏ(Mô đun),mỗi mô đun là 1 hàm
- Ý nghĩa:
    +Sử dụng lại được code mà ko cần viết lại
    +Làm cho chương trình trở lên dễ dàng hơn
    +Dễ duy trì,phục hồi,xử lý,...
- Cú pháp hàm:
<giá trị trả về><Tên hàm>(<Tham số nếu có>)
{
    Thân hàm
}
-Tham số là những giá trị truyền vào hàm để tính toán và xử lý
-Ví dụ:
    int main()  //Giá trị trả về là kiểu int, có tên là main, ko có tham số
    {
        cout<<"Hello";
        return 0;
    }
*/